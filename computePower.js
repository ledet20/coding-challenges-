// Given a number and an exponent, "computePower" returns the given number, 
// raised to the given exponent. 

function computePower(num, exponent) {
  return Math.pow(num, exponent);  // javascript built in function MAth.pow() takes in two parameters 
                                  // the base number and the exponenet you want to raise it to 
}

computePower(2,2); // 4
